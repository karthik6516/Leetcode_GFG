class Solution {
  public:
    long long int minOperations(int N) {
        // Code here
        if(N%2==0)
        {
            long long mid=N/2;
            return mid*mid;
        }
        else
        {
           long long e=N/2; 
           return e*(e+1);
        }
    }
};

